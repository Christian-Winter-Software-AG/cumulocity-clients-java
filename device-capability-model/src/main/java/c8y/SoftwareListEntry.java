package c8y;

import java.util.Map;

import org.svenson.AbstractDynamicProperties;

public class SoftwareListEntry extends AbstractDynamicProperties {
    
    private static final long serialVersionUID = 196308789576755511L;

    private String name;
    
    private String version;
    
    private String url;
    
    public SoftwareListEntry() {
    }
    
    public SoftwareListEntry(Map<String,String> map) {
        this.name = map.get("name");
        this.version = map.get("version");
        this.url = map.get("url");
    }
    
    public SoftwareListEntry(String name, String version, String url) {
        this.name = name;
        this.version = version;
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        result = prime * result + ((url == null) ? 0 : url.hashCode());
        result = prime * result + ((version == null) ? 0 : version.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        SoftwareListEntry other = (SoftwareListEntry) obj;
        if (name == null) {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;
        if (url == null) {
            if (other.url != null)
                return false;
        } else if (!url.equals(other.url))
            return false;
        if (version == null) {
            if (other.version != null)
                return false;
        } else if (!version.equals(other.version))
            return false;
        return true;
    }

    @Override
    public String toString() {
        return "SoftwareListEntry [name=" + name + ", version=" + version + ", url=" + url + "]";
    }
    
}
